package viradonojiraya.devdojo.variaveis;

public class CalculadoraDiferencaIdade {
    public static void main(String[] args) {
        int primeiraIdade = 67;
        int segundaIdade = 30;
        int diferencaIdade = primeiraIdade - segundaIdade;

        System.out.println("A primeiraIdade é = " + primeiraIdade + " anos, sendo a segundaIdade = " +
                segundaIdade + " anos, a difenreça é de " + diferencaIdade + " anos.");
    }
}
